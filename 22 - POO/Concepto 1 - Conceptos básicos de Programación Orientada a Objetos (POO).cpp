// Clase = Una abstracción que hacemos de nuestra experiencia sensible.

// Objeto = Es un conjunto de atributos y métodos, un objeto se deriva de una clase.
// - Atributos = Características.
// - Métodos = Acciones.

// Abstracción = Proceso mental de extracción de las características esenciales de algo, ignorando los detalles superfluos.

// Encapsulación = Proceso por el que se ocultan los detalles del soporte de las características de una abstracción.

// Herencia = Es donde una clase nueva se crea a partir de una clase existente, heredando todos sus atributos y métodos.

// Polimorfismo = Aquella cualidad que poseen los objetos para responder de distinto modo ante el mismo mensaje.

